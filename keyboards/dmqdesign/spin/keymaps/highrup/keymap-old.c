void oled_task_user(void) {
    if (timer_elapsed32(oled_timer) > 45000) {
        oled_off();
    } else if (timer_elapsed32(oled_timer) > 20000) {
        if (IS_LAYER_ON(_MID)) {
            render_dead_logo();
        } else if (IS_LAYER_ON(_RAISE)) {
            render_media_logo();
        } else if (IS_LAYER_ON(_ADJUST)) {
            render_acrnym_logo();
        } else if (IS_LAYER_ON(_GAME)) {
            render_wow_logo();
        } else if (IS_LAYER_ON(_PHOTOSHOP)) {
            render_akira_logo();
        } else if (IS_LAYER_ON(_INDESIGN)) {
            render_akira_logo();
        } else {
            render_statement_logo();
            oled_scroll_left();
        }
    } else {
        // Host Keyboard Layer Status
        switch (get_highest_layer(layer_state)) {
            case _INDESIGN:
                render_status_indesign();
                break;
            case _PHOTOSHOP:
                render_status_pshop();
                break;
            case _GAME:
                render_status_games();
                break;
            case _ADJUST:
                render_status_adjust();
                break;
            case _RAISE:
                render_status_media();
                break;
            case _MID:
                render_status_secondary();
                break;
            default:
                render_status_main();
                break;
        }
        oled_scroll_off();
    }
    update_log();
}
