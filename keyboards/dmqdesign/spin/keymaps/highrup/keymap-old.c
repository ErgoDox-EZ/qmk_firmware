bool process_record_user(uint16_t keycode, keyrecord_t *record) {
switch (keycode) {
  case ENABLE_APPSWITCHER:
    if (record->event.pressed) {
      key_timer = timer_read();  // start the timer
      appswitcher_enabled = true;
      encoder_enabled = true;
      register_code16(KC_LALT);
      tap_code16(KC_TAB);
      tap_code16(LSFT(KC_TAB));
    } else {
      if (timer_elapsed(key_timer) < TAPPING_TERM) {
        appswitcher_enabled = false;
        encoder_enabled = true;
        mute_enabled = true;
        unregister_code16(KC_LALT);
        tap_code16(KC_MUTE);
        encoder_enabled = false;
      } else if (timer_elapsed(key_timer) < TAPPING_TERM + 500) {
        appswitcher_enabled = false;
        encoder_enabled = true;
        unregister_code16(KC_LALT);
        selected_layer = 0;
        layer_clear();
        encoder_enabled = false;
    } else {
      appswitcher_enabled = false;
      encoder_enabled = false;
      unregister_code16(KC_LALT);
      }
    }
    return false;
    break;
  case ENABLE_TABSWITCHER:
    if (record->event.pressed) {
      key_timer = timer_read();  // start the timer
      tabswitcher_enabled = true;
      encoder_enabled = true;
      register_code16(KC_LCTL);
      tap_code16(KC_TAB);
      tap_code16(LSFT(KC_TAB));
    } else {
      if (timer_elapsed(key_timer) < TAPPING_TERM) {
        tabswitcher_enabled = false;
        encoder_enabled = false;
        unregister_code16(KC_LCTL);
      	tap_code16(LCTL(KC_R));
    } else {
      tabswitcher_enabled = false;
      encoder_enabled = false;
      unregister_code16(KC_LCTL);
      }
    }
    return false;
    break;
  case ENABLE_MEDIA:
    if (record->event.pressed) {
      key_timer = timer_read();  // start the timer
      media_enabled = true;
      encoder_enabled = true;
    } else {
      if (timer_elapsed(key_timer) < TAPPING_TERM) {
        media_enabled = false;
        encoder_enabled = false;
        tap_code(KC_MPLY);
    } else {
      media_enabled = false;
      encoder_enabled = false;
      }
    }
    return false;
    break;
  case ENABLE_ADJUST:
    if (record->event.pressed) {
      adjust_enabled = true;
      encoder_enabled = true;
    } else {
      adjust_enabled = false;
      encoder_enabled = false;
    }
    return false;
    break;
  case ENABLE_GAME:
    if (record->event.pressed) {
      key_timer = timer_read();  // start the timer
      game_enabled = true;
      encoder_enabled = true;
      register_code16(KC_LCTL);
    } else {
      if (timer_elapsed(key_timer) < TAPPING_TERM) {
        game_enabled = false;
        encoder_enabled = false;
        unregister_code16(KC_LCTL);
      } else {
        game_enabled = false;
        encoder_enabled = false;
        unregister_code16(KC_LCTL);
      }
    }
    return false;
    break;
  case ENABLE_GAME2:
    if (record->event.pressed) {
      key_timer = timer_read();  // start the timer
      game2_enabled = true;
      encoder_enabled = true;
      register_code16(KC_LSFT);
    } else {
      if (timer_elapsed(key_timer) < TAPPING_TERM) {
        game2_enabled = false;
        encoder_enabled = false;
        unregister_code16(KC_LSFT);
        tap_code(KC_ESC);
      } else {
        game2_enabled = false;
        encoder_enabled = false;
        unregister_code16(KC_LSFT);
      }
    }
    return false;
    break;
  case ENABLE_PSHOP:
    if (record->event.pressed) {
      key_timer = timer_read();  // start the timer
      pshop_enabled = true;
      encoder_enabled = true;
    } else {
      if (timer_elapsed(key_timer) < TAPPING_TERM) {
        pshop_enabled = false;
        encoder_enabled = false;
        tap_code(KC_B);
      } else {
        pshop_enabled = false;
        encoder_enabled = false;
      }
    }
    return false;
    break;
  case ENABLE_PSHOP2:
    if (record->event.pressed) {
      key_timer = timer_read();  // start the timer
      pshop2_enabled = true;
      encoder_enabled = true;
    } else {
      if (timer_elapsed(key_timer) < TAPPING_TERM) {
        pshop2_enabled = false;
        encoder_enabled = false;
        tap_code(KC_B);
      } else {
        pshop2_enabled = false;
        encoder_enabled = false;
      }
    }
    return false;
    break;
  case ENABLE_INDES:
    if (record->event.pressed) {
      key_timer = timer_read();  // start the timer
      indes_enabled = true;
      encoder_enabled = true;
    } else {
      if (timer_elapsed(key_timer) < TAPPING_TERM) {
        indes_enabled = false;
        encoder_enabled = false;
        tap_code(KC_NO);
      } else {
        indes_enabled = false;
        encoder_enabled = false;
      }
    }
    return false;
    break;
  case ENABLE_INDES2:
    if (record->event.pressed) {
      key_timer = timer_read();  // start the timer
      indes2_enabled = true;
      encoder_enabled = true;
    } else {
      if (timer_elapsed(key_timer) < TAPPING_TERM) {
        indes2_enabled = false;
        tap_code(KC_NO);
      } else {
        indes2_enabled = false;
      }
    }
    return false;
    break;
  case MC_QMK:
    if(record->event.pressed) {
      key_timer = timer_read();  // start the timer
      return false;
    } else {
      if (timer_elapsed(key_timer) < TAPPING_TERM) {
        SEND_STRING ("cd C:/qmk_firmware"); // Change the character(s) to be sent on tap here
        tap_code(KC_ENT);
        return false;
      } else {
        SEND_STRING ("make dmqdesign/spin:highrup"); // Change the character(s) to be sent on hold here
        tap_code(KC_ENT);
        return true;
      }
    }
    break;
  }
  if (record->event.pressed) {
    #ifdef OLED_DRIVER_ENABLE
      oled_timer = timer_read32();
      add_keylog(keycode);
    #endif
  }
  return true;
}

void encoder_update_user(uint8_t index, bool clockwise) {
oled_timer = timer_read32();
uint8_t currentLayer = biton32(layer_state);
  if (index == 0) { /* First encoder */
  //break each encoder update into a switch statement for the current layer
    switch (currentLayer) {
      case _PHOTOSHOP:
        if (pshop_enabled) {
          if (!clockwise) {
            tap_code16(LSFT(KC_MINS));
          } else {
            tap_code16(LSFT(KC_EQL));
          }
        } else {
          uint8_t mods = get_mods();
          if (mods & MOD_MASK_SHIFT) {
            if (!clockwise) {
              unregister_mods(MOD_MASK_SHIFT);
              if (opacity < 0 + opacity_increment) {
                opacity = 0;
              } else {
                opacity -= opacity_increment;
              }
            } else {
              unregister_mods(MOD_MASK_SHIFT);
              if (opacity > 100 - opacity_increment) {
                opacity = 100;
              } else {
                opacity += opacity_increment;
              }
            }
            if(mods & MOD_BIT(KC_LSFT)){
              register_code(KC_LSFT);
            }
            if(mods & MOD_BIT(KC_RSFT)){
              register_code(KC_RSFT);
            }

            uint8_t opacity_to_type = opacity;
              if (opacity_to_type == 0) {
                opacity_to_type = 1;
              }

              uint8_t last = opacity_to_type % 10;
              uint8_t first = opacity_to_type / 10 % 10;
              uint8_t digits[2] = {first, last};
              for(int i = 0; i < 2; ++i) {
                switch (digits[i]) {
                case 0: tap_code(KC_0); break;
                case 1: tap_code(KC_1); break;
                case 2: tap_code(KC_2); break;
                case 3: tap_code(KC_3); break;
                case 4: tap_code(KC_4); break;
                case 5: tap_code(KC_5); break;
                case 6: tap_code(KC_6); break;
                case 7: tap_code(KC_7); break;
                case 8: tap_code(KC_8); break;
                case 9: tap_code(KC_9); break;
              }
            }
          } else {
            if (!clockwise) {
              tap_code16(LCTL(KC_Z));
            } else {
              tap_code16(LSFT(LCTL(KC_Z)));
            }
          }
        }
        break;
      case _GAME:
        if (game_enabled) {
          if (!clockwise) {
            tap_code16(KC_TAB);
          } else {
            tap_code16(LSFT(KC_TAB));
          }
        } else {
          if (!clockwise) {
            tap_code(KC_TAB);
          } else {
            tap_code16(LSFT(KC_TAB));
          }
        }
        break;
      default:
        if (tabswitcher_enabled) {
          if (!clockwise) {
            tap_code16(LSFT(KC_TAB));
          } else {
            tap_code16(KC_TAB);
          }
        } else {
          uint8_t mods = get_mods();
          if (mods & MOD_MASK_SHIFT) {
            if (!clockwise) {
              tap_code(KC_WBAK);
            } else {
              tap_code(KC_WFWD);
            }
          } else {
            if (!clockwise) {
              tap_code16(LCTL(KC_PGUP));
            } else {
              tap_code16(LCTL(KC_PGDN));
            }
          }
        }
        break;
    }
  } else if (index == 1) { /* Second encoder */
    switch (currentLayer) {
      case _PHOTOSHOP:
        if (pshop2_enabled) {
          if (!clockwise) {
            tap_code16(LSFT(KC_RBRC));
          } else {
            tap_code16(LSFT(KC_LBRC));
          }
        } else {
          if (!clockwise) {
            tap_code(KC_LBRC);
          } else {
            tap_code(KC_RBRC);
          }
        }
        break;
      case _GAME:
        if (game2_enabled) {
          if (!clockwise) {
            tap_code(KC_RBRC);
          } else {
            tap_code(KC_LBRC);
          }
        } else {
          if (!clockwise) {
            tap_code16(KC_WH_D);
          } else {
            tap_code16(KC_WH_U);
          }
        }
        break;
      case _RAISE:
        if (media_enabled) {
          if (!clockwise) {
            tap_code(KC_MPRV);
          } else {
            tap_code(KC_MNXT);
          }
        } else {
          uint8_t mods = get_mods();
          if (mods & MOD_MASK_SHIFT) {
            if (!clockwise) {
              unregister_mods(MOD_MASK_SHIFT);
              tap_code(KC_LEFT);
            } else {
              unregister_mods(MOD_MASK_SHIFT);
              tap_code(KC_RIGHT);
            }
            if(mods & MOD_BIT(KC_LSFT)){
              register_code(KC_LSFT);
            }
            if(mods & MOD_BIT(KC_RSFT)){
              register_code(KC_RSFT);
            }
          } else {
            if (!clockwise) {
              tap_code16(KC_NO);
            } else {
              tap_code16(KC_NO);
            }
          }
        }
        break;
      case _ADJUST:
        if (adjust_enabled) {
          if (!clockwise) {
            tap_code16(RGB_RMOD);
          } else {
            tap_code16(RGB_MOD);
          }
        } else {
          uint8_t mods = get_mods();
          if (mods & MOD_MASK_SHIFT) {
            if (!clockwise) {
              rgblight_decrease_val();
            } else {
              rgblight_increase_val();
            }
          } else {
            if (!clockwise) {
              rgblight_decrease_hue();
            } else {
              rgblight_increase_hue();
            }
          }
        }
        break;
      default:
        if (media_enabled) {
          if (!clockwise) {
            tap_code(KC_NO);
          } else {
            tap_code(KC_NO);
          }
        } else {
          uint8_t mods = get_mods();
          if (mods & MOD_MASK_SHIFT) {
            if (!clockwise) {
              unregister_mods(MOD_MASK_SHIFT);
              tap_code(KC_LEFT);
            } else {
              unregister_mods(MOD_MASK_SHIFT);
              tap_code(KC_RIGHT);
            }
            if(mods & MOD_BIT(KC_LSFT)){
              register_code(KC_LSFT);
            }
            if(mods & MOD_BIT(KC_RSFT)){
              register_code(KC_RSFT);
            }
          } else {
            if (!clockwise) {
              tap_code16(KC_WH_U);
            } else {
              tap_code16(KC_WH_D);
            }
          }
        }
        break;
    }
  } else if (index == 2) { /* Third encoder */
    switch (currentLayer) {
      default:
        if (appswitcher_enabled) {
          if (!clockwise) {
            tap_code16(LSFT(KC_TAB));
          } else {
            tap_code16(KC_TAB);
          }
        } else {
          uint8_t mods = get_mods();
          if (mods & MOD_MASK_SHIFT) {
            if (!clockwise) {
              selected_layer --;
            } else {
              selected_layer ++;
            }
            layer_clear();
            layer_on(selected_layer % 7);
          } else {
            if (!clockwise) {
              tap_code(KC_VOLU);
              mute_enabled = false;
            } else {
              tap_code(KC_VOLD);
              mute_enabled = false;
            }
          }
        }
        break;
      }
   }
}

